    void updateList() {
        updateList = pl.getListSong();
        DefaultListModel model = new DefaultListModel();
        for (int i = 0; i < updateList.size(); i++) {
            int j = i + 1;
            model.add(i, j + " | " + ((File) updateList.get(i)).getName());
        }
        jList1.setModel(model);

    }
    
    //INI GROUP PLAYLIST
    void add() {
        pl.add(this);
        updateList();
    }

    void remove() {
        try {
            int akan_dhps = jList1.getLeadSelectionIndex();
            pl.ls.remove(akan_dhps);
            updateList();
        } catch (Exception e) {

        }
    }

    void up() {
        try {
            int s1 = jList1.getLeadSelectionIndex();
            simpan = (File) pl.ls.get(s1);
            pl.ls.remove(s1);
            pl.ls.add(s1 - 1, simpan);
            updateList();
            jList1.setSelectedIndex(s1 - 1);
        } catch (Exception e) {

        }
    }
    
    void down(){
        try {
            int s1 = jList1.getLeadSelectionIndex();
            simpan = (File) pl.ls.get(s1);
            pl.ls.remove(s1);
            pl.ls.add(s1 + 1, simpan);
            updateList();
            jList1.setSelectedIndex(s1 + 1);
        } catch (Exception e) {

        }
    }
    
    void save(){
        pl.saveAsPlaylist(this);
        updateList();
    }
    
    void open(){
        pl.openPls(this);
        updateList();
    }
    
    //INI GROUP KONTROL
    File play1;
    static int a = 0;
    void putar() {
        if (a == 0) {
            try {
                int p1 = jList1.getSelectedIndex();
                play1 = (File) this.updateList.get(p1);
                FileInputStream fis = new FileInputStream(play1);
                BufferedInputStream bis = new BufferedInputStream(fis);
                player = new Player(bis);
                a = 1;
            } catch (Exception e) {
                System.out.println("Problem playing file ");
                System.out.println(e);
            }
            // run in new thread to play in background
            new Thread() {
                @Override
                public void run() {
                    try {
                        player.play();
                        //player.play();

                    } catch (Exception e) {
                        System.out.println(e);
                    }
                }
            }.start();
        } else {
            player.close();
            a = 0;
            putar();
        }

    }
    
